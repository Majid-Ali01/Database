SELECT 
    country,
    age,
    COUNT(age) AS occurance,
    SUM(score) AS total_score
FROM sectioa.studrent
GROUP BY country, age
ORDER BY country, age;

SELECT 
    country,
    COUNT(*) AS occurance,
    SUM(score) AS total_score,
    AVG(score) AS average_score,
    MIN(score) AS minimum_score,
    MAX(score) AS maximum_score
FROM sectioa.studrent
GROUP BY country
ORDER BY country;
